    private void evtDangNhap() {
        String getTaiKhoan = txtTaiKhoan.getText();
        String getMatKhau = new String(txtMatKhau.getText());
        StringBuilder sp = new StringBuilder();
        if (getTaiKhoan.isBlank() || getMatKhau.isEmpty()) {
            sp.append("Tài khoản không được để trống!\n");
        }
        if (getMatKhau.isBlank() || getMatKhau.isEmpty()) {
            sp.append("Mật khẩu không được để trống!\n");
        }
        if (sp.length() == 0) {
            _iLogin.login(getTaiKhoan, getMatKhau);
            if (Tags.Login._nhanVien != null) {
                File file = new File("Install", "Password");
                if (chkLuuMatKhau.isSelected()) {
                    file.mkdirs();
                    file = new File("Install\\Password\\RememberPassword.dat");
                    try {
                        if (!file.exists()) {
                            file.createNewFile();
                        }
                        FileOutputStream fos = new FileOutputStream(file);
                        ObjectOutputStream oos = new ObjectOutputStream(fos);
                        oos.writeObject(Tags.Login._nhanVien);
                        fos.close();
                        oos.close();
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(FormDangNhap.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(FormDangNhap.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    file = new File("Install\\Password\\RememberPassword.dat");
                    if (file.exists()) {
                        file.delete();
                    }
                }
                new FormMain().setVisible(true);
                this.dispose();
            } else {
                Tags.MsgThongBao.alert(this, "Đăng nhập thất bại!");
            }
        } else {
            Tags.MsgThongBao.alert(this, sp.toString());
        }
    }

