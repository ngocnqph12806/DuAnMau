    private void setAcountRemember() {
        File file = new File("Install\\Password\\RememberPassword.dat");
        if (file.exists()) {
            try {
                ObjectInputStream ois = new ObjectInputStream(new FileInputStream(file));
                if (ois.hashCode() > -1) {
                    NhanVien nv = (NhanVien) ois.readObject();
                    if (nv != null) {
                        txtTaiKhoan.setText(nv.getMaNV());
                        txtMatKhau.setText(nv.getMatKhau());
                        chkLuuMatKhau.setSelected(true);
                    }
                }
                ois.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(FormDangNhap.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(FormDangNhap.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FormDangNhap.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

